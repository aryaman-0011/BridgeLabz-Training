package com.json.jsonobject;

import java.util.List;

public class Student {
    private String name;
    private int age;
    private List<String> subjects;

    // getters and setters
    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public List<String> getSubjects() {
        return subjects;
    }
}
